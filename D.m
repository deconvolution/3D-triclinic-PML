function u2=D(u,d)
%% 3rd order
%{
u2=zeros(size(u));
switch d
    case 1
        % backward
        u2=-1/3*ad(u(1:end-3,:,:),3,-1)...
            +3/2*ad(u(1:end-2,:,:),2,-1) ...
            -3*ad(u(1:end-1,:,:),1,-1) ...
            +11/6*u;
    case -1
        % forward
        u2=-11/6*u ...
            +3*ad(u(2:end,:,:),1,1) ...
            -3/2*ad(u(3:end,:,:),2,1) ...
            +1/3*ad(u(4:end,:,:),3,1);
    case 2
        u2=-1/3*ad(u(:,1:end-3,:),3,-2)...
            +3/2*ad(u(:,1:end-2,:),2,-2) ...
            -3*ad(u(:,1:end-1,:),1,-2) ...
            +11/6*u;
    case -2
        u2=-11/6*u ...
            +3*ad(u(:,2:end,:),1,2) ...
            -3/2*ad(u(:,3:end,:),2,2) ...
            +1/3*ad(u(:,4:end,:),3,2);
    case 3
        u2=-1/3*ad(u(:,:,1:end-3),3,-3)...
            +3/2*ad(u(:,:,1:end-2),2,-3) ...
            -3*ad(u(:,:,1:end-1),1,-3) ...
            +11/6*u;
    case -3
        u2=-11/6*u ...
            +3*ad(u(:,:,2:end),1,3) ...
            -3/2*ad(u(:,:,3:end),2,3) ...
            +1/3*ad(u(:,:,4:end),3,3);
end
%}
%% 2nd order
%{
u2=zeros(size(u));
switch d
    case 1
        % backward
        u2=1/2*ad(u(1:end-2,:,:),2,-1) ...
            -2*ad(u(1:end-1,:,:),1,-1) ...
            +3/2*u;
    case -1
        % forward
        u2=-3/2*u ...
            +2*ad(u(2:end,:,:),1,1) ...
            -1/2*ad(u(3:end,:,:),2,1);
    case 2
        u2=1/2*ad(u(:,1:end-2,:),2,-2) ...
            -2*ad(u(:,1:end-1,:),1,-2) ...
            +3/2*u;
    case -2
        u2=-3/2*u ...
            +2*ad(u(:,2:end,:),1,2) ...
            -1/2*ad(u(:,3:end,:),2,2);
    case 3
        u2=1/2*ad(u(:,:,1:end-2),2,-3) ...
            -2*ad(u(:,:,1:end-1),1,-3) ...
            +3/2*u;
    case -3
        u2=-3/2*u ...
            +2*ad(u(:,:,2:end),1,3) ...
            -1/2*ad(u(:,:,3:end),2,3);
end
%}
%% first order

u2=zeros(size(u));
switch d
    case 1
        % backward
        u2=-1*ad(u(1:end-1,:,:),1,-1) ...
            +1*u;
    case -1
        % forward
        u2=-1*u ...
            +1*ad(u(2:end,:,:),1,1);
    case 2
        u2=-1*ad(u(:,1:end-1,:),1,-2) ...
            +1*u;
    case -2
        u2=-1*u ...
            +1*ad(u(:,2:end,:),1,2);
    case 3
        u2=-1*ad(u(:,:,1:end-1),1,-3) ...
            +1*u;
    case -3
        u2=-1*u ...
            +1*ad(u(:,:,2:end),1,3);
end

